// CRC16-CCITT False
// Poly: 0x1021, Init: 0xFFFF, RefIn: False, RefOut: False, XorOut: 0x0000
uint16_t wenet_crc16_ccitt_false(const uint8_t *buf, size_t len) {
    uint16_t crc = 0xFFFF;
    for (size_t i = 0; i < len; i++) {
        crc ^= (uint16_t)buf[i] << 8;
        for (int j = 0; j < 8; j++) {
            if (crc & 0x8000) {
                crc = (crc << 1) ^ 0x1021;
            } else {
                crc = crc << 1;
            }
        }
    }
    return crc;
}

void wenet_scramble(uint8_t *buf, size_t len) {
    for (size_t i = 0; i < len; i++) {
        buf[i] ^= scramble_code[i % sizeof(scramble_code)];
    }
}

size_t wenet_build_frame(const uint8_t payload[256], uint8_t out_frame[343]) {
    // 1. Preamble: 16 bytes of 0x55
    memset(out_frame, 0x55, 16);

    // 2. Sync Word (UW): 0xAB 0xCD 0xEF 0x01
    out_frame[16] = 0xAB;
    out_frame[17] = 0xCD;
    out_frame[18] = 0xEF;
    out_frame[19] = 0x01;

    // 3. Payload: 256 bytes
    memcpy(&out_frame[20], payload, 256);

    // 4. CRC over payload (little-endian per spec Appendix A)
    uint16_t crc = wenet_crc16_ccitt_false(payload, 256);
    out_frame[276] = (uint8_t)(crc & 0xFF);
    out_frame[277] = (uint8_t)((crc >> 8) & 0xFF);

    // 5. LDPC Parity: 65 bytes (rate-0.8 RA code over Payload+CRC = 258 bytes)
    // The parity bits are computed over the 256 payload bytes + 2 CRC bytes
    wenet_ldpc_encode(&out_frame[20], &out_frame[278]);

    // 6. Scramble: Payload+CRC+Parity (323 bytes) XORed with repeating PN sequence
    wenet_scramble(&out_frame[20], 323);

    return 343;
}

void wenet_generate_idle_payload(uint8_t payload[256]) {
    payload[0] = 0x56; // Packet type
    memset(&payload[1], 0x56, 255);
}
